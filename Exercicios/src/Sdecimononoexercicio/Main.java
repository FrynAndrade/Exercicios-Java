public class Main {
    public static void main(String[] args) {
        // 19 - Faça um algoritmo que imprima na tela a tabuada de 1 até 10.
        int numero = 1;

        for (int i = 0; i <= 10; i++) {
            System.out.println(numero + " x " + i + " = " + (numero * i));
        }
    }
}
